function checarCor(cor) {
  const r = parseInt(cor.substr(0, 2), 16);

  const g = parseInt(cor.substr(2, 2), 16);

  const b = parseInt(cor.substr(4, 2), 16);

  const luminosidade = (r * 299 + g * 587 + b * 114) / 1000;

  if (luminosidade > 125) return "#000000";
  else return "#ffffff";
}
